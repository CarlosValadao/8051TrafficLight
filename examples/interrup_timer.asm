;Using the timer (TH0) with interrupt.

	ORG 0
	LJMP MAIN
	ORG 000BH
	LJMP ISRT0
	ORG 0050H

MAIN:	MOV TH0, #0FFH
	MOV TL0, #0FAH
	MOV TMOD, #00000001B
	MOV IE, #10000010B
	SETB TR0

LOOP:   SJMP LOOP
        ORG 0100H
	
	
ISRT0: CPL P2.0
       MOV TH0, #0FFH
       MOV TL0, #0FAH
       RETI
       END